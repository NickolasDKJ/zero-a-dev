import math
import random

#math - funçoes matemáticas
print(math.sqrt(16))         # raiz quadrada
print(math.pi)               # o número pi
print(math.ceil(4.2))        # arredonda pra cima
print(math.floor(4.8))       # arredonda pra baixo

# random - númeors aleatórios
print(random.randint(1, 10))  # númeor aleatório entre 1 e 10
print(random.choice(["cada", "coroa"]))   # escolhe aleatório de uma lista
